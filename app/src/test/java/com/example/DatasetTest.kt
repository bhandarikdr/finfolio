    @Test
    fun testBuyAmountCalculation() {
        val lines = csvData.split("\n")
        val header = parseCsvRow(lines[0])
        val itemIdx = header.indexOf("Item")
        val actionIdx = header.indexOf("Action")
        val amountIdx = header.indexOf("Amount")

        val transactions = mutableListOf<TransactionRecord>()
        for (i in 1 until lines.size) {
            val cols = parseCsvRow(lines[i])
            if (cols.size > amountIdx) {
                val item = cols[itemIdx].trim()
                val action = cols[actionIdx].trim()
                val amountStr = cols[amountIdx].replace("[^0-9.]".toRegex(), "")
                val amount = amountStr.toDoubleOrNull() ?: 0.0
                
                transactions.add(
                    TransactionRecord(
                        item = item,
                        action = action,
                        amount = amount,
                        date = "", type = ""
                    )
                )
            }
        }

        val metrics = FinancialEngines.computeItemMetrics(transactions, emptyList())
        
        val nabilMetrics = metrics.find { it.item == "NABIL" }
        val adblMetrics = metrics.find { it.item == "ADBL" }

        println("NABIL Buy Amount: ${nabilMetrics?.buyAmount}")
        println("ADBL Buy Amount: ${adblMetrics?.buyAmount}")

        // Expected values calculated manually:
        // NABIL: 18229.38 + 17057.38 + 22288.18 = 57574.94
        // ADBL: 7248.85 + 7123.52 = 14372.37
        
        assertEquals(57574.94, nabilMetrics?.buyAmount ?: 0.0, 0.01)
        assertEquals(14372.37, adblMetrics?.buyAmount ?: 0.0, 0.01)
    }

    private fun parseCsvRow(rowText: String): List<String> {
        val tokens = mutableListOf<String>()
        var curToken = StringBuilder()
        var insideQuotes = false
        var i = 0
        while (i < rowText.length) {
            val c = rowText[i]
            if (c == '"') {
                insideQuotes = !insideQuotes
            } else if (c == ',' && !insideQuotes) {
                tokens.add(curToken.toString().trim())
                curToken = StringBuilder()
            } else {
                curToken.append(c)
            }
            i++
        }
        tokens.add(curToken.toString().trim())
        return tokens
    }
